#include <stdio.h>

int	ft_str_is_lowercase(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] < 'a' || str[i] > 'z')
			return (0);
		i++;
	}
	return (1);
}

int	main(void)
{
	char	c[] = "190";
	char	*p;

	p = c;
	printf("%s = %d\n", c, ft_str_is_lowercase(p));
}
